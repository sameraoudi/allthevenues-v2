<article class="atv-legal portal-guide">
  <h1>Partner Portal guide</h1>

  <h2>Welcome to the Partner Portal</h2>
  <p>This is where you manage your venues on All The Venues. You can add new venues, keep venue details up to
    date, upload photos, claim venues you operate, and request changes to listings that are already live.</p>
  <p>To protect the quality of the directory, changes may be reviewed by All The Venues before they appear
    publicly. You&rsquo;ll always be able to see the current status of each venue, and we&rsquo;ll email you when
    a decision is made.</p>

  <h2>Managing your venues</h2>
  <p>Open a venue from My Venues to see its details and current status.</p>
  <p>You can edit most day-to-day details, including capacity, layouts, facilities, pricing notes, descriptions,
    and photos. Changes are saved in the portal and may be reviewed by All The Venues before they appear
    publicly.</p>
  <p>Some sensitive details are handled differently. These include the venue name, URL/slug, venue type, primary
    location or emirate, and event types once the venue is published. Changes to these details are submitted as
    change requests and reviewed by our team before they appear on the public site.</p>
  <p>Your venue stays exactly as it is until the request is approved.</p>

  <h2>Adding a new venue</h2>
  <p>Adding a venue takes three steps:</p>
  <ol>
    <li><strong>Enter the details.</strong> Fill in the venue&rsquo;s information. This is saved as a private
      draft. It is not public yet.</li>
    <li><strong>Add at least one photo.</strong> A venue needs at least one photo before it can be submitted for
      review.</li>
    <li><strong>Submit for review.</strong> Once the required details and a photo are in place, submit the venue.
      Our team reviews it, and it stays private until it is approved and published.</li>
  </ol>
  <p>You can save a draft and come back to finish it at any time.</p>

  <h2>Photos and image rights</h2>
  <p>When you upload photos, you confirm that you own them or have permission to use them.</p>
  <p>Please only upload images you have the rights to use. Do not upload photos taken from other websites unless
    you have permission.</p>
  <p>All The Venues reviews each photo for quality and image rights before it appears on your public listing. We
    may reject images that are unclear, low quality, watermarked, duplicated, rights-restricted, or unsuitable
    for the venue listing.</p>
  <p>Photo submissions may show their own status, such as Pending review, Approved, Rejected, or Withdrawn.</p>

  <h2>Claiming a venue</h2>
  <p>If a venue you operate is already listed but is not linked to your account, you can claim it from the Claims
    page.</p>
  <p>Tell us who you are, your role, and anything that helps us verify that you are authorised to manage the
    venue. Useful proof may include a work email on the venue&rsquo;s domain, a link to your role on the
    venue&rsquo;s website, or a management agreement.</p>
  <p>If more than one party is involved, contested claims are reviewed carefully before any venue is
    reassigned.</p>

  <h2>Requesting changes to a published venue</h2>
  <p>For sensitive details, use Request changes on the venue.</p>
  <p>You propose the new values, and they are sent to All The Venues for review. Nothing on the public listing
    changes until the request is approved.</p>
  <p>You&rsquo;ll see a Pending note while the request is with us. You can withdraw the request before it is
    reviewed, and we&rsquo;ll email you the outcome.</p>

  <h2>Delisting and re-listing</h2>
  <p>Need to take a venue offline for a while? You can request delisting on a published venue.</p>
  <p>Once approved, the venue is hidden from the public site. It will not appear in search, event-type pages,
    location pages, or new enquiry flows. Your venue data, photos, and past enquiries are kept.</p>
  <p>When you&rsquo;re ready, you can request to re-list the venue from the portal. If no further review is
    needed, it can be made live again quickly.</p>

  <h2>What happens after you submit</h2>
  <p>Each venue shows a status so you always know where it stands.</p>
  <ul>
    <li><strong>Draft</strong> — Private and not yet submitted.</li>
    <li><strong>Under review</strong> — Submitted and with All The Venues for review.</li>
    <li><strong>Changes requested</strong> — We&rsquo;ve asked for a few changes before the venue can go live.
      Update it and re-submit.</li>
    <li><strong>Live</strong> — Published and visible on the public site.</li>
    <li><strong>Delisted</strong> — Hidden from the public site. You can request to re-list it when ready.</li>
  </ul>
  <p>We&rsquo;ll email you whenever a decision is made, so you don&rsquo;t have to keep checking back.</p>

  <h2>Need help?</h2>
  <p>Questions this guide doesn&rsquo;t answer? <a href="<?= e(base_url('contact')) ?>">Contact</a> All The Venues.</p>
</article>
