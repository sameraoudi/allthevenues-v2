<article class="atv-legal portal-guide">
  <h1>Partner Portal guide</h1>

  <h2>Welcome to the Partner Portal</h2>
  <p>This is where you manage your venues on All The Venues — add new venues, keep their details up to date,
    upload photos, claim venues you operate, and request changes to listings that are already live. To protect
    the quality of the directory, <strong>every change is reviewed by All The Venues before it goes public</strong>.
    You&rsquo;ll always be able to see the current status of each venue, and we email you when a decision is made.</p>

  <h2>Managing your venues</h2>
  <p>Open a venue from <strong>My Venues</strong> to see its details and current status. You can edit most
    day-to-day details — capacity, layouts, facilities, pricing, description and photos — and those save live.</p>
  <p>A few sensitive details are handled differently. The venue&rsquo;s <strong>name, type, location (emirate)</strong>,
    and its <strong>event types once it&rsquo;s published</strong>, are proposed as a <strong>change request</strong>
    and reviewed by our team before they change on the public site. Your venue stays exactly as it is until the
    request is approved.</p>

  <h2>Adding a new venue</h2>
  <p>Adding a venue takes three steps:</p>
  <ul>
    <li><strong>1. Enter the details.</strong> Fill in the venue&rsquo;s information. This is saved as a
      <strong>private draft</strong> — only you can see it, and it&rsquo;s never public yet.</li>
    <li><strong>2. Add at least one photo.</strong> A venue needs a photo before it can be submitted.</li>
    <li><strong>3. Submit for review.</strong> Once the required details and a photo are in place, submit the
      venue. Our team reviews it, and it stays private until it&rsquo;s approved and published.</li>
  </ul>
  <p>You can save a draft and come back to finish it at any time.</p>

  <h2>Photos &amp; image rights</h2>
  <p>When you upload photos you confirm that you own them or have permission to use them. Please only upload
    images you have the rights to — don&rsquo;t use photos taken from other websites without permission. All The
    Venues reviews each photo for <strong>quality and rights</strong> before it appears on your public listing,
    and we may not publish images that don&rsquo;t meet those standards.</p>

  <h2>Claiming a venue</h2>
  <p>If a venue you operate is already listed but isn&rsquo;t on your account, you can <strong>claim</strong> it
    from the Claims page. Tell us who you are and your role, and add anything that helps us verify you&rsquo;re
    authorised — for example a work email on the venue&rsquo;s domain, a link to your role on the venue&rsquo;s
    website, or a management agreement. If more than one party is involved, <strong>contested claims are reviewed
    carefully</strong> before any venue is reassigned.</p>

  <h2>Requesting changes to a published venue</h2>
  <p>For the sensitive details listed above, use <strong>Request changes</strong> on the venue. You propose the
    new values, and they&rsquo;re sent to All The Venues for review — nothing on the public listing changes yet.
    You&rsquo;ll see a &ldquo;pending&rdquo; note on the venue while it&rsquo;s with us, you can withdraw the
    request before it&rsquo;s reviewed, and we email you the outcome.</p>

  <h2>Delisting &amp; re-listing</h2>
  <p>Need to take a venue offline for a while? Request <strong>delisting</strong> on a published venue and,
    once approved, it&rsquo;s hidden from the public site — it won&rsquo;t appear in search or on event-type
    pages, and new enquiries pause. Your data, photos and past enquiries are all kept. When you&rsquo;re ready,
    you can <strong>re-list it yourself</strong> from the portal at any time and it goes live again straight away.</p>

  <h2>What happens after you submit</h2>
  <p>Each venue shows a status so you always know where it stands:</p>
  <ul>
    <li><strong>Draft</strong> — private and not yet submitted.</li>
    <li><strong>Under review</strong> — submitted and with All The Venues for review.</li>
    <li><strong>Changes requested</strong> — we&rsquo;ve asked for a few changes before it can go live; update it
      and re-submit.</li>
    <li><strong>Live</strong> — published and visible on the public site.</li>
    <li><strong>Delisted</strong> — hidden from the public site; you can re-list it anytime.</li>
  </ul>
  <p>We&rsquo;ll email you whenever a decision is made, so you don&rsquo;t have to keep checking back.</p>

  <p class="lead-hint">Questions this guide doesn&rsquo;t answer? <a href="<?= e(base_url('contact')) ?>">Contact All The Venues</a>.</p>
